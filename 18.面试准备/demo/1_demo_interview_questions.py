"""
回答思路：
先说明幻觉的类型，再介绍应对策略，最后给出实际案例。

参考回答：
遇到过幻觉问题。LLM 幻觉主要有三种类型：

1. 事实性幻觉
   - 生成与事实不符的内容
   - 例子："地球到月球的距离是 50 万公里"（实际 38.4 万公里）

2. 忠实性幻觉
   - 生成与上下文不符的内容
   - 例子：上下文说"1989 年开发"，回答说"1991 年创建"

3. 推理性幻觉
   - 推理过程错误
   - 例子："A 比 B 大，B 比 C 大"，推断"C 比 A 大"

我的应对策略：

1. Prompt 约束
   - 明确要求"只基于上下文回答"
   - 提供"不知道"的出口
   - 要求引用来源

2. Temperature 控制
   - 设为 0，减少随机性

3. Relevance 阈值过滤
   - 过滤低相关度文档（阈值 0.7）
   - 减少噪声干扰

4. 来源引用
   - 要求回答中标注 [1], [2] 等来源
   - 用户可以验证

5. 后处理验证
   - 生成后验证回答是否基于上下文
   - 检测数字、日期等事实

效果：
Faithfulness 从 0.7 提升到 0.92

加分话术：
"在我的项目中，我采用多层防御策略。
最有效的是 Prompt 约束 + 来源引用的组合。
Faithfulness 从 0.7 提升到 0.92。"

常见追问：
Q: Temperature 设为 0 还是有幻觉怎么办？
A: "更根本的方法是提供更相关的上下文、后处理验证、如果验证失败回复'无法回答'。"
"""


