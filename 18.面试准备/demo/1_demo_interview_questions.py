# 问题 4：向量检索的原理是什么？HNSW 大致怎么工作？
# ============================================================

"""
回答思路：
先解释向量检索的基本原理，再说明 HNSW 的工作方式。

参考回答：
向量检索的原理：

1. 向量化
   - 用 Embedding 模型将文本转换为高维向量（如 1536 维）
   - 语义相似的文本，向量距离近

2. 相似度计算
   - 余弦相似度：cos(A, B) = (A·B) / (|A|×|B|)
   - 值越接近 1，越相似

3. 暴力搜索
   - 计算查询向量与所有文档向量的相似度
   - 返回 Top-K 最相似的文档
   - 问题：文档量大时太慢（O(n) 复杂度）

HNSW（Hierarchical Navigable Small World）：

HNSW 是一种近似最近邻（ANN）算法，能在牺牲少量精度的情况下大幅提升检索速度。

核心思想：
1. 构建多层图结构
   - 底层：包含所有节点，每个节点连接最近的 K 个邻居
   - 上层：稀疏的导航层，用于快速定位

2. 搜索过程
   - 从最顶层开始，贪心地跳到最近的邻居
   - 逐层下降，每层都做贪心搜索
   - 到达底层时，找到近似最近邻

3. 为什么快？
   - 不需要遍历所有节点
   - 通过图的"跳跃"快速定位
   - 时间复杂度：O(log n)

类比：
- 暴力搜索：在图书馆逐本书翻找
- HNSW：先看楼层指示牌（上层），再看区域指示（中层），最后在书架上找（底层）

加分话术：
"向量检索的核心是将文本转换为向量，然后计算向量距离。
但暴力搜索在大规模数据下太慢，所以我使用了 HNSW 索引。
HNSW 通过构建多层图结构，实现 O(log n) 的检索速度。"

常见追问：
Q: HNSW 的参数怎么调？
A: "主要参数是 M（每个节点的连接数）和 ef_construction（构建时的搜索范围）。
M 越大精度越高但内存越大，ef_construction 越大构建越慢但质量越好。"
"""


