# 问题 1：什么是 RAG？为什么需要 RAG 而不是直接用 LLM？
# ============================================================

"""
回答思路：
先解释 RAG 的定义，再说明直接用 LLM 的问题，最后强调 RAG 的优势。

参考回答：
RAG（Retrieval-Augmented Generation）是检索增强生成的缩写。
它的核心思想是：在 LLM 生成回答之前，先从知识库中检索相关文档，
然后将检索结果作为上下文提供给 LLM，让 LLM 基于这些上下文生成回答。

为什么需要 RAG 而不是直接用 LLM？

1. 知识时效性
   - LLM 的知识截止到训练数据，无法获取最新信息
   - RAG 可以实时检索最新文档，知识随时更新

2. 领域专业性
   - LLM 对特定领域（如企业内部知识）了解有限
   - RAG 可以引入领域专有知识，提升专业性

3. 减少幻觉
   - LLM 可能编造不存在的信息
   - RAG 基于检索到的文档生成，有据可依

4. 可解释性
   - LLM 是黑盒，无法追溯答案来源
   - RAG 可以引用来源文档，便于验证

5. 成本效益
   - Fine-tuning 需要标注数据和 GPU 资源
   - RAG 不需要训练，成本更低

加分话术：
"在实际项目中，我选择 RAG 主要基于三个考虑：
知识更新频率、成本效益、可解释性。
我们的知识库每周都有更新，如果用 Fine-tuning 每周都要重新训练，成本太高。"

常见追问：
Q: RAG 和 Fine-tuning 能结合吗？
A: "可以。先用 Fine-tuning 让模型适应特定领域，再用 RAG 引入最新知识。
这种组合在实际项目中效果很好。"
"""


