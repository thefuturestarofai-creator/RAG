"""
背景与需求（2-3分钟）：

"首先介绍一下项目背景。

我们的业务场景是 [公司内部知识库问答]。
员工在工作中经常需要查阅技术文档、产品手册、规章制度等资料。
这些资料分散在多个系统中，包括 Confluence、SharePoint、本地文件服务器。

面临的问题：
1. 信息分散：资料分布在 5+ 个系统中
2. 检索困难：需要记住在哪个系统找，平均检索时间 5 分钟
3. 版本混乱：不知道哪个是最新版本

项目目标：
构建一个统一的智能问答系统，实现：
1. 多源文档统一管理
2. 自然语言问答
3. 检索时间从 5 分钟降低到 30 秒以内
"""


# ============================================================
# 第三部分：技术选型
# ============================================================

"""
技术选型（3-4分钟）：

"接下来讲一下技术选型。

首先，为什么选 RAG 而不是 Fine-tuning？

我考虑了三个因素：
1. 知识更新：我们的知识库每周都有更新，RAG 可以实时更新，Fine-tuning 需要重新训练
2. 成本：Fine-tuning 需要标注数据和 GPU 资源，RAG 成本更低
3. 可解释性：RAG 可以引用来源，便于用户验证

技术栈选择：

1. Embedding 模型：BGE-large-zh
   - 原因：中文效果最好，MTEB 中文排行榜前三
   - 对比：测试了 text-embedding-ada-002、M3E-large

2. 向量数据库：Chroma
   - 原因：开源免费、易于部署、LangChain 原生支持
   - 备选：如果规模扩大，可以切换到 Milvus

3. LLM：GPT-3.5-Turbo
   - 原因：性价比高，响应速度快
   - 备选：关键场景可以用 GPT-4

4. 框架：LangChain
   - 原因：生态完善、社区活跃、文档齐全

5. API 服务：FastAPI
   - 原因：性能优秀、自动文档生成、异步支持"
"""


# ============================================================
# 第四部分：架构设计
# ============================================================

"""
架构设计（5-6分钟）：

"接下来介绍系统架构。

整体架构分为五个模块：

1. 文档处理模块
   - 功能：解析 PDF、Word、网页等多种格式
   - 技术：PyPDF2、python-docx、BeautifulSoup
   - 输出：结构化文本

2. 分块模块
   - 功能：将长文档切分成小块
   - 策略：递归分块 + 滑动窗口（overlap 20%）
   - 参数：块大小 512 tokens

3. 向量化模块
   - 功能：将文本转换为向量
   - 模型：BGE-large-zh（1024 维）
   - 存储：Chroma 向量数据库

4. 检索模块
   - 功能：根据查询检索相关文档
   - 策略：混合检索（BM25 + 向量）+ Re-ranking
   - 参数：召回 Top-50，重排取 Top-5

5. 生成模块
   - 功能：基于检索结果生成回答
   - 策略：Prompt 工程 + Temperature=0
   - 特性：支持流式输出、来源引用

数据流：
用户提问 → Query 改写 → 混合检索 → Re-ranking → Prompt 构造 → LLM 生成 → 返回回答
"""


# ============================================================
# 第五部分：难点与优化
# ============================================================

"""
难点与优化（10-12分钟）：

"接下来重点介绍项目中遇到的技术难点和解决方案。

难点1：检索准确率低

问题描述：
初始版本使用单一向量检索，准确率只有 60%。
用户反馈经常找不到相关文档，或者找到的文档不相关。

分析原因：
1. 向量检索对精确匹配不敏感
2. Embedding 模型对某些领域术语理解不准确
3. 分块策略导致上下文丢失

解决方案：
引入混合检索 + Re-ranking 的两阶段架构

第一阶段：混合检索
- BM25：擅长精确匹配，对专有名词效果好
- 向量检索：擅长语义理解，对同义词效果好
- RRF 融合：结合两者优势

第二阶段：Re-ranking
- 使用 Cross-Encoder 对候选文档重排序
- 模型：BGE-Reranker-base
- 输入：Top-50 候选，输出：Top-5 最终结果

效果：
检索准确率从 60% 提升到 85%，提升 42%。

---

难点2：幻觉问题

问题描述：
LLM 偶尔会生成错误信息，比如编造不存在的功能、错误的数字。

分析原因：
1. LLM 的固有局限性
2. Prompt 约束不够严格
3. 检索到的文档不够相关

解决方案：
多层防御策略

第一层：Prompt 约束
- 明确要求"只基于上下文回答"
- 提供"不知道"的出口
- 要求引用来源

第二层：Temperature 控制
- 设为 0，减少随机性

第三层：Relevance 阈值
- 过滤低相关度文档（阈值 0.7）

第四层：后处理验证
- 验证回答是否基于上下文
- 检测数字、日期等事实

效果：
Faithfulness 从 0.7 提升到 0.92。

---

难点3：长文档处理

问题描述：
我们的文档很多是长篇技术文档，有的一章就有上万字。
如果按固定大小分块，会切断语义；
如果按章节分块，块太大，检索不精确。

解决过程：
1. 第一版：固定大小分块（512 tokens）
   - 问题：经常切断句子，上下文丢失

2. 第二版：按段落分块
   - 问题：段落大小不一，有的太短有的太长

3. 第三版：递归分块 + 滑动窗口
   - 先按章节切分，再按段落细分
   - 块之间有 20% 重叠
   - 效果：语义完整性保持，检索准确率提升 20%

经验总结：
分块策略没有银弹，需要根据文档特点和业务需求选择。
关键是建立评估体系，用数据驱动优化。"
"""


